#include "../includes/libft.h"

size_t		ft_icount(int n)
{
	int cmp;
	int a;

	cmp = 1;
	a = n;
	if (n < 0)
		cmp++;
	while (a > 9 || a < -9)
	{
		a = a / 10;
		cmp++;
	}
	return (cmp);
}
